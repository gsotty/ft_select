#include "./ft_select.h"

void	free_buf(t_buf *buf)
{
	int		x;

	x = 0;
	while (buf->tab_arg[x] != NULL)
	{
		free(buf->tab_arg[x]);
		x++;
	}
	free(buf->tab_arg);
	free(buf->nbr);
}
