#include "./ft_select.h"

void	all_select(t_buf *buf)
{
	int		x;

	x = 0;
	while (x < buf->argc)
	{
		if (buf->nbr[x] == 0)
			buf->nbr[x] = 1;
		else
			buf->nbr[x] = 0;
		x++;
	}
}
